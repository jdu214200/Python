class УчетЗадач:
    def __init__(self):
        self.задачи = []

    def добавить_задачу(self, задача):
        self.задачи.append(задача)
        print(f"Задача '{задача}' добавлена.")

    def удалить_задачу(self, задача):
        if задача in self.задачи:
            self.задачи.remove(задача)
            print(f"Задача '{задача}' удалена.")
        else:
            print(f"Задачи '{задача}' нет в списке.")

    def показать_задачи(self):
        if self.задачи:
            print("Список задач:")
            for номер, задача in enumerate(self.задачи, start=1):
                print(f"{номер}. {задача}")
        else:
            print("Список задач пуст.")

# Пример использования
учет_задач = УчетЗадач()

учет_задач.добавить_задачу("Подготовить отчет")
учет_задач.добавить_задачу("Провести собрание")
учет_задач.показать_задачи()

учет_задач.удалить_задачу("Подготовить отчет")
учет_задач.показать_задачи()
